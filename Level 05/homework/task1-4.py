# 2) მომხმარებელს შემოატანინეთ ორი რიცხვი და შეასრულეთ მათემატიკური ოპერაციები
num1 = float(input("შეიყვანე პირველი რიცხვი: "))
num2 = float(input("შეიყვანე მეორე რიცხვი: "))

print(f"ჯამი (+): {num1 + num2}")
print(f"სხვაობა (-): {num1 - num2}")
print(f"ნამრავლი (*): {num1 * num2}")
print(f"განაყოფი (/): {num1 / num2}")
print(f"ნაშთი (%): {num1 % num2}")
print(f"ხარისხი (**): {num1 ** num2}")

print(f"ნაკლებია? (<): {num1 < num2}")
print(f"მეტია? (>): {num1 > num2}")
print(f"ნაკლები ან ტოლია? (<=): {num1 <= num2}")
print(f"მეტი ან ტოლია? (>=): {num1 >= num2}")
print(f"ტოლია? (==): {num1 == num2}")
print(f"არ ტოლია? (!=): {num1 != num2}")

