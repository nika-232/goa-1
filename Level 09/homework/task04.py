# მომხმარებლისგან რიცხვის მიღება
number = int(input("10: "))

# დაბეჭდვა 0-დან (ან 1-დან) რიცხვამდე
for i in range(number + 1):
    print(i)



# input() იღებს მნიშვნელობას სტრიქონის სახით

# int() ფუნქცია გარდაქმნის მას მთელ რიცხვად (explicit conversion)

# range(number + 1) ქმნის რიცხვების მიმდევრობას 0-დან number-ის ჩათვლით

# for ციკლი თითოეული რიცხვისთვის იყენებს ცვლადს i და ბეჭდავს მას